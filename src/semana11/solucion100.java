package semana11;

public class solucion100 {
    public static int sumar(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 20;
        System.out.println("La suma de " + numero1 + " y " + numero2 + " es: " +
                sumar(numero1, numero2));
    }
}